void resizeGridData (grid_t& gridData, size_t const (&dim)[3])
{
    gridData.resize(dim[0]);
    for (auto& slab: gridData)
    {
        slab.resize(dim[1]);
        for (auto& line: slab)
        {
            line.resize(dim[2]);
        }
    }
}

template <class It>
std::string join (It begin, It const& end, std::string const& divStr="")
{
    std::string output{""};
    while (begin != end)
    {
        output.append(std::to_string(*begin++)+divStr);
    }
    return output;
}

