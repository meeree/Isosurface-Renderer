__device__ inline float3 getPosition (Grid* grid, unsigned i, unsigned j, unsigned k)
{
    return {
        (*grid).mOrigin[0]+(float)i/(*grid).mDim[0]*(*grid).mSize[0],
        (*grid).mOrigin[1]+(float)j/(*grid).mDim[1]*(*grid).mSize[1],
        (*grid).mOrigin[2]+(float)k/(*grid).mDim[2]*(*grid).mSize[2]};
}

__device__ inline float3 vertexInterp (float isolevel, float3 p1, float3 p2, float valp1, float valp2)
{
	return p1 + (isolevel-valp1)*(p2-p1)/(valp2-valp1);
}

__device__ float sampleNoise (float3 p)
{
    return Simplex::noise(glm::vec3(p.x,p.y,p.z));
}

#define OCT_CNT 5
static __device__ float frequencies []
{
    1.0f, 0.5f, 0.1f, 0.13f, 2.0f
};

static __device__ float amplitudes []
{
    0.15f, 0.35f, 2.0f, -1.0f, 0.05f
};

__device__ float sample (float3 p)
{
    glm::vec3 pVec{p.x,p.y,p.z};
    float rad = 7.5f;
    glm::vec3 offset{pVec - glm::vec3(0.0f,-rad,0.0f)};
    float density = rad-glm::length(offset);
    for (int i = 0; i < OCT_CNT; ++i)
    {
        density += sampleNoise(p*frequencies[i])*amplitudes[i];
    }
//    density -= glm::clamp((0.5f-p.y)*3.0f, 0.0f, 1.0f)*40;
//    density += glm::clamp((-1.0f-p.y+0.1f*cos(5.0f*(cos(p.x)-p.z))-sampleNoise(p*0.4f)*0.5f)*2.0f, 0.0f, 1.0f)*40;
//    density -= glm::clamp((1.0f-p.y)*3.0f, 0.0f, 1.0f)*40;
    return density;
}

__global__ void polygonise(Grid* grid, float isolevel, unsigned zIndex, float3* vertices)
{
	unsigned gridIndex[3]{
		blockIdx.x*blockDim.x + threadIdx.x,
		blockIdx.y*blockDim.y + threadIdx.y,
        zIndex};

    if (gridIndex[0] > (*grid).mDim[0] || gridIndex[1] > (*grid).mDim[1] || gridIndex[2] > (*grid).mDim[2])
        return;
//    unsigned vertexIndex{15*((gridIndex[2]*(unsigned)(*grid).mDim[1]+gridIndex[1])*(unsigned)(*grid).mDim[0] + gridIndex[0])};
    unsigned vertexIndex{15*(gridIndex[1]*(unsigned)(*grid).mDim[0] + gridIndex[0])};

	float3 positions[8]{
		getPosition(grid, gridIndex[0], gridIndex[1], gridIndex[2]+1),
		getPosition(grid, gridIndex[0]+1, gridIndex[1], gridIndex[2]+1),
		getPosition(grid, gridIndex[0]+1, gridIndex[1], gridIndex[2]),
		getPosition(grid, gridIndex[0], gridIndex[1], gridIndex[2]),
		getPosition(grid, gridIndex[0], gridIndex[1]+1, gridIndex[2]+1),
		getPosition(grid, gridIndex[0]+1, gridIndex[1]+1, gridIndex[2]+1),
		getPosition(grid, gridIndex[0]+1, gridIndex[1]+1, gridIndex[2]),
		getPosition(grid, gridIndex[0], gridIndex[1]+1, gridIndex[2])};
	float values[8]{
        sample(positions[0]),
        sample(positions[1]),
        sample(positions[2]),
        sample(positions[3]),
        sample(positions[4]),
        sample(positions[5]),
        sample(positions[6]),
        sample(positions[7])};

	unsigned cubeindex = 0;
	if (values[0] < isolevel) cubeindex |= 1;
	if (values[1] < isolevel) cubeindex |= 2;
	if (values[2] < isolevel) cubeindex |= 4;
	if (values[3] < isolevel) cubeindex |= 8;
	if (values[4] < isolevel) cubeindex |= 16;
	if (values[5] < isolevel) cubeindex |= 32;
	if (values[6] < isolevel) cubeindex |= 64;
	if (values[7] < isolevel) cubeindex |= 128;

	if (cubeindex == 0)
    {
        for (unsigned i = 0; i < 15; ++i)
        {
            vertices[vertexIndex+i] = make_float3(nanf(""), nanf(""), nanf(""));
        }
		return;
    }

	float3 vertlist[12]{
		vertexInterp(isolevel,positions[0],positions[1],values[0],values[1]),
		vertexInterp(isolevel,positions[1],positions[2],values[1],values[2]),
		vertexInterp(isolevel,positions[2],positions[3],values[2],values[3]),
		vertexInterp(isolevel,positions[3],positions[0],values[3],values[0]),
		vertexInterp(isolevel,positions[4],positions[5],values[4],values[5]),
		vertexInterp(isolevel,positions[5],positions[6],values[5],values[6]),
		vertexInterp(isolevel,positions[6],positions[7],values[6],values[7]),
		vertexInterp(isolevel,positions[7],positions[4],values[7],values[4]),
		vertexInterp(isolevel,positions[0],positions[4],values[0],values[4]),
		vertexInterp(isolevel,positions[1],positions[5],values[1],values[5]),
		vertexInterp(isolevel,positions[2],positions[6],values[2],values[6]),
		vertexInterp(isolevel,positions[3],positions[7],values[3],values[7])};

	for (unsigned i = 0; i < 15; i += 3) 
	{
        if (triTable[cubeindex][i] == -1)
        {
            for (unsigned j = i; j < 15; ++j)
            {
                vertices[vertexIndex+j] = make_float3(nanf(""), nanf(""), nanf(""));
            }
            break;
        }
		vertices[vertexIndex+i] = vertlist[triTable[cubeindex][i]];
		vertices[vertexIndex+i+1] = vertlist[triTable[cubeindex][i+1]];
		vertices[vertexIndex+i+2] = vertlist[triTable[cubeindex][i+2]];
	}
}

void fillSpace (std::vector<Vertex>& glVertices)
{
    float3 vertices[15*16*16];
    float3* d_vertices;
    Grid* d_grid;
    
//    vertices = (float3*)malloc(15*16*16*sizeof(float3));
    cudaMalloc(&d_grid, sizeof(Grid));
    cudaMalloc(&d_vertices, 15*16*16*sizeof(float3));
    dim3 blockCnt{1, 1, 1};
    dim3 threadsPerBlock{16, 16, 1};

    unsigned sz = 30;
    for (unsigned k = 0; k < sz; ++k)
    {
        for (unsigned j = 0; j < 30; ++j)
        {
            for (unsigned i = 0; i < sz; ++i)
            {
                Grid grid{{16, 16, 16}, {1.0f, 1.0f, 1.0f}, {-(float)sz/2+(float)i, -15.0f+(float)j, -(float)sz/2+(float)k}};
                cudaMemcpy(d_grid, &grid, sizeof(Grid), cudaMemcpyHostToDevice);
    
                for (unsigned slice = 0; slice < 16; ++slice)
                {
                    polygonise<<<blockCnt, threadsPerBlock>>>(d_grid, 0.0, slice, d_vertices);
                    cudaMemcpy(vertices, d_vertices, 15*16*16*sizeof(float3), cudaMemcpyDeviceToHost);
                    for (auto const& vert: vertices)
                    {
                        if (!std::isnan(vert.x))
                            glVertices.push_back({{vert.x, vert.y, vert.z},{0,0,0}});
                    }
                }
            }
        }
    }

    cudaFree(d_vertices);
    cudaFree(d_grid);
}
